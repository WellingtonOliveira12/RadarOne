/**
 * Retorna UA aleatório do pool
 */
export function randomUA(): string {
  const index = Math.floor(Math.random() * userAgents.length);
  return userAgents[index];
}

/**
 * Retorna UA consistente baseado em seed (mesmo seed = mesmo UA)
 * Útil para manter mesmo UA durante uma sessão
 */
export function seedUA(seed: string): string {
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    const char = seed.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash = hash & hash; // Convert to 32bit integer
  }

  const index = Math.abs(hash) % userAgents.length;
  return userAgents[index];
}

